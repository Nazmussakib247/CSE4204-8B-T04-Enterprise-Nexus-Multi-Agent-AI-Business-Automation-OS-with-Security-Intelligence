const supabase = require('../config/supabase');
const { generateAnalyticsReport } = require('../utils/gemini');
const { writeAuditLog } = require('../utils/audit');

// GET /api/analytics/reports
const getReports = async (req, res, next) => {
  try {
    const { page = 1, limit = 20 } = req.query;
    const offset = (page - 1) * limit;

    const { data, error, count } = await supabase
      .from('analytics_reports')
      .select('*', { count: 'exact' })
      .eq('user_id', req.user.id)
      .order('created_at', { ascending: false })
      .range(offset, offset + Number(limit) - 1);

    if (error) throw error;
    res.json({ data, total: count, page: Number(page), limit: Number(limit) });
  } catch (err) {
    next(err);
  }
};

// GET /api/analytics/reports/:id
const getReport = async (req, res, next) => {
  try {
    const { data, error } = await supabase
      .from('analytics_reports')
      .select('*')
      .eq('id', req.params.id)
      .eq('user_id', req.user.id)
      .single();

    if (error || !data) return res.status(404).json({ error: 'Report not found' });
    res.json({ data });
  } catch (err) {
    next(err);
  }
};

// POST /api/analytics/reports — manual create
const createReport = async (req, res, next) => {
  try {
    const { performance_rating, overall_score, ai_insights, action_items, kpi_snapshot } = req.body;

    const { data, error } = await supabase
      .from('analytics_reports')
      .insert({
        user_id: req.user.id,
        performance_rating,
        overall_score,
        ai_insights,
        action_items,
        kpi_snapshot,
      })
      .select()
      .single();

    if (error) throw error;
    res.status(201).json({ message: 'Analytics report created', data });
  } catch (err) {
    next(err);
  }
};

// POST /api/analytics/generate — Gemini cross-module KPI report
const generateReport = async (req, res, next) => {
  try {
    const userId = req.user.id;

    // Pull data from all modules for this user
    const [hrRes, financeRes, supportRes] = await Promise.all([
      supabase.from('hr_reports').select('recommendation, ai_score, job_title').eq('user_id', userId).order('created_at', { ascending: false }).limit(50),
      supabase.from('finance_records').select('category, amount, severity').eq('user_id', userId).order('created_at', { ascending: false }).limit(50),
      supabase.from('support_tickets').select('urgency, sentiment, status, escalated').eq('user_id', userId).order('created_at', { ascending: false }).limit(50),
    ]);

    const aiResult = await generateAnalyticsReport({
      hr: hrRes.data || [],
      finance: financeRes.data || [],
      support: supportRes.data || [],
    });

    const { data, error } = await supabase
      .from('analytics_reports')
      .insert({
        user_id: userId,
        performance_rating: aiResult.performance_rating,
        overall_score: aiResult.overall_score,
        ai_insights: aiResult.ai_insights,
        action_items: aiResult.action_items,
        kpi_snapshot: aiResult.kpi_snapshot,
      })
      .select()
      .single();

    if (error) throw error;

    writeAuditLog({
      userId,
      action: 'analytics.report.generate',
      resourceType: 'analytics_report',
      resourceId: data.id,
      req,
    });

    res.status(201).json({ message: 'Analytics report generated by AI', data, ai_analysis: aiResult });
  } catch (err) {
    next(err);
  }
};

// GET /api/analytics/kpi — latest KPI snapshot
const getLatestKPI = async (req, res, next) => {
  try {
    const { data, error } = await supabase
      .from('analytics_reports')
      .select('kpi_snapshot, overall_score, performance_rating, created_at')
      .eq('user_id', req.user.id)
      .order('created_at', { ascending: false })
      .limit(1)
      .single();

    if (error || !data) return res.status(404).json({ error: 'No KPI data found' });
    res.json({ data });
  } catch (err) {
    next(err);
  }
};

// DELETE /api/analytics/reports/:id
const deleteReport = async (req, res, next) => {
  try {
    const { error } = await supabase
      .from('analytics_reports')
      .delete()
      .eq('id', req.params.id)
      .eq('user_id', req.user.id);

    if (error) throw error;
    res.json({ message: 'Report deleted' });
  } catch (err) {
    next(err);
  }
};

module.exports = { getReports, getReport, createReport, generateReport, getLatestKPI, deleteReport };
